    public function index(Request $request)
    {
       $type = $request->type;
        $q = $request->q;

        switch ($type){
            case 1:
                $services = NULL;
               $projects= Project::where('name','LIKE','%'.$q.'%')->orWhere('skills','LIKE','%'.$q.'%')->get();


              return view('results',compact('projects','services'));
            case 2:
                $services= Service::where('name','LIKE','%'.$q.'%')->orWhere('category','LIKE','%'.$q.'%')->get();


                $projects = NULL;

                return view('results',compact('services','projects'));

            default:
            return abort(404);
        }



    }

